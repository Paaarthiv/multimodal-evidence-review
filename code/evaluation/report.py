"""Render evaluation_report.md from evaluation results.

Includes the metric comparison (>=2 model configs), the claim_status confusion
matrix, and the required operational analysis (model calls, tokens, images,
projected cost for the full test set, runtime, and TPM/RPM strategy).
"""
from __future__ import annotations

from pathlib import Path

# Dataset facts (counts) used to project sample -> test.
SAMPLE_ROWS = 20
TEST_ROWS = 44
SAMPLE_IMAGES = 31
TEST_IMAGES = 85


def _md_table(headers, rows):
    out = ["| " + " | ".join(headers) + " |",
           "| " + " | ".join("---" for _ in headers) + " |"]
    for r in rows:
        out.append("| " + " | ".join(str(c) for c in r) + " |")
    return "\n".join(out)


def write_report(results: list, path: Path) -> None:
    best = max(results, key=lambda r: (r["metrics"]["full_row_exact_match"],
                                       r["metrics"]["scalar_accuracy"]["claim_status"]))
    lines = []
    lines.append("# Evaluation Report — Multi-Modal Evidence Review\n")
    lines.append("Generated by `code/evaluation/main.py`. Metrics are computed on the "
                 "20 labelled rows in `dataset/sample_claims.csv`.\n")

    # ---- Comparison table (>=2 configs) ----
    lines.append("## 1. Model configuration comparison\n")
    headers = ["Config", "claim_status acc", "full-row exact", "risk_flags F1",
               "severity MAE", "calls", "in/out tok", "runtime (s)", "cost (USD)"]
    rows = []
    for r in results:
        m, op = r["metrics"], r["operational"]
        rows.append([
            r["provider"],
            m["scalar_accuracy"]["claim_status"],
            m["full_row_exact_match"],
            m["multilabel"]["risk_flags"]["f1"],
            m.get("severity_ordinal_mae"),
            op["model_calls"],
            f'{op["input_tokens"]}/{op["output_tokens"]}',
            op["elapsed_sec"],
            op["cost"]["total_cost"],
        ])
    lines.append(_md_table(headers, rows))
    lines.append(f"\n**Selected final strategy:** `{best['provider']}`. The production run "
                 "in `code/main.py` uses this via `PROVIDER_ORDER`.\n")

    # ---- Experiment log / configuration comparison (real journey) ----
    lines.append("### 1b. Configuration comparison & experiment log\n")
    lines.append("Configurations evaluated during development (≥2 model configs + prompt "
                 "strategies), with why we landed on a local model:\n")
    lines.append(_md_table(
        ["Config", "Type", "claim_status acc", "Outcome / why not final"],
        [
            ["OpenAI gpt-4o", "cloud", "n/a", "Account had no billing quota (insufficient_quota) — could not run."],
            ["Gemini 2.5-flash", "cloud", "n/a", "Free tier overloaded (503) + 20 req/day/model cap — unreliable."],
            ["Gemini 2.5-flash-lite", "cloud", "~0.75", "Best observed, but free-tier daily cap (20/day) exhausted before a full run; billing unavailable."],
            ["Qwen2.5-VL 7B (Ollama)", "local", "see table above", "FINAL: free, no keys, no rate limits, fully reproducible."],
        ]))
    lines.append("\nPrompt-strategy iterations (measured on the gold set):\n")
    lines.append("- v1 baseline → v2 add explicit allowed-token lists (fixed out-of-vocab "
                 "`issue_type` like `impact_damage`) → v3 add severity rubric + a strict "
                 "contradicted-vs-NEI decision order → v4 compact JSON output contract for "
                 "the local model (reliability).\n")

    # ---- Per-column accuracy for the best config ----
    lines.append("## 2. Per-column accuracy (best config)\n")
    sa = best["metrics"]["scalar_accuracy"]
    lines.append(_md_table(["Column", "Accuracy"], [[k, v] for k, v in sa.items()]))
    ml = best["metrics"]["multilabel"]
    lines.append("\n**Multi-label / set columns:**\n")
    lines.append(_md_table(
        ["Column", "Precision", "Recall", "F1", "Exact-set match"],
        [[c, ml[c]["precision"], ml[c]["recall"], ml[c]["f1"], ml[c]["exact_set_match"]]
         for c in ml]))

    # ---- Confusion matrix ----
    lines.append("\n## 3. claim_status confusion matrix (best config)\n")
    conf = best["metrics"]["confusion"]["claim_status"]
    labels = ["supported", "contradicted", "not_enough_information"]
    cm_rows = []
    for g in labels:
        row = [f"gold={g}"] + [conf.get(g, {}).get(p, 0) for p in labels]
        cm_rows.append(row)
    lines.append(_md_table(["", *[f"pred={p}" for p in labels]], cm_rows))

    wrong = best["metrics"]["examples_wrong"]["claim_status"]
    if wrong:
        lines.append("\n**Misclassified rows (status):**\n")
        lines.append(_md_table(["row", "user_id", "gold", "pred"],
                               [[w["row"], w["user_id"], w["gold"], w["pred"]] for w in wrong]))

    # ---- Operational analysis ----
    op = best["operational"]
    calls_per_row = (op["model_calls"] / SAMPLE_ROWS) if SAMPLE_ROWS else 1
    in_per_call = (op["input_tokens"] / op["model_calls"]) if op["model_calls"] else 0
    out_per_call = (op["output_tokens"] / op["model_calls"]) if op["model_calls"] else 0
    proj_calls = round(calls_per_row * TEST_ROWS)
    proj_in = round(in_per_call * proj_calls)
    proj_out = round(out_per_call * proj_calls)
    price = op["cost"]
    proj_cost = round(proj_in / 1e6 * price["input_per_mtok"] + proj_out / 1e6 * price["output_per_mtok"], 4)
    sec_per_row = (op["elapsed_sec"] / SAMPLE_ROWS) if SAMPLE_ROWS else 0

    lines.append("\n## 4. Operational analysis\n")
    lines.append(f"- **Architecture:** exactly **one vision call per claim** (all images of a "
                 f"case batched into a single call). ~{calls_per_row:.2f} calls/row.")
    lines.append(f"- **Model calls:** sample run = {op['model_calls']} calls for {SAMPLE_ROWS} rows "
                 f"({op['cache_hits']} cache hits). Projected test run ≈ **{proj_calls} calls** "
                 f"for {TEST_ROWS} rows.")
    lines.append(f"- **Tokens:** sample = {op['input_tokens']} in / {op['output_tokens']} out "
                 f"(~{in_per_call:.0f}/{out_per_call:.0f} per call). "
                 f"Projected test ≈ {proj_in} in / {proj_out} out.")
    lines.append(f"- **Images processed:** {SAMPLE_IMAGES} (sample) + {TEST_IMAGES} (test) = "
                 f"{SAMPLE_IMAGES + TEST_IMAGES} total; downscaled to ≤{_max_edge()}px longest edge "
                 "before upload to cut image tokens.")
    lines.append(f"- **Approximate cost (assumed model `{price['assumed_model']}`, "
                 f"${price['input_per_mtok']}/M in, ${price['output_per_mtok']}/M out):** "
                 f"sample ≈ ${price['total_cost']}, **full test set ≈ ${proj_cost}**.")
    lines.append(f"- **Latency / runtime:** sample took {op['elapsed_sec']}s "
                 f"(~{sec_per_row:.1f}s/row at concurrency); full test ≈ "
                 f"{round(sec_per_row * TEST_ROWS)}s wall-clock with parallelism.")
    lines.append("- **Rate limits / reliability strategy:**")
    lines.append("  - Final run is **fully local (Ollama)** → no API rate limits and **$0 cost**; "
                 "`MAX_CONCURRENCY=1` because a single local model cannot truly parallelize.")
    lines.append("  - Provider/model failover is built in (`PROVIDER_ORDER`, e.g. local → "
                 "Gemini → OpenAI) for the cloud path; quota/daily-cap errors fail over "
                 "immediately, per-minute/5xx errors retry with exponential backoff.")
    lines.append("  - On-disk response cache keyed by (provider, model, prompt, image bytes) so "
                 "re-runs, eval iterations, and resumed/interrupted runs never repay for "
                 "identical calls (also enables crash-safe resume of the long local run).")
    lines.append("  - Local-model tuning for tractable CPU inference: plain JSON mode (not "
                 "schema-grammar), compact output contract, `num_ctx` sized for multi-image, "
                 "and 512px image downscaling.")
    lines.append("\n> Measured figures above come from the actual run; if this report was "
                 "regenerated from cache, see `code/logs/last_run.json` for the real test-set "
                 "call/token/latency numbers.")

    lines.append("\n## 5. Notes\n")
    lines.append("- No hardcoded per-case answers: `sample_claims.csv` is used only for scoring "
                 "and prompt iteration, never as a lookup table.")
    lines.append("- Decision policy (evidence_standard_met tied to verdict; history additive only; "
                 "NEI ⇒ severity/issue unknown; injection ⇒ `text_instruction_present`) is "
                 "deterministic and lives in `src/fusion.py`.")

    path.write_text("\n".join(lines) + "\n", encoding="utf-8")


def _max_edge():
    from src import config
    return config.IMAGE_MAX_EDGE
